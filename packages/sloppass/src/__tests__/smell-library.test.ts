import { describe, expect, it } from "vitest";
import {
  DEFAULT_SLOPPASS_SMELL_CHECKS,
  detectSlopSmells,
  getSlopPassSmellChecks,
} from "../smell-library.js";

describe("SlopPass smell library", () => {
  it("ships the fixture-only chunk-1 smell checks", () => {
    expect(DEFAULT_SLOPPASS_SMELL_CHECKS.map((check) => check.id)).toEqual([
      "placeholder-logic",
      "broad-any-bypass",
      "unsafe-dynamic-execution",
      "secret-like-literal",
      "catch-all-fallback",
      "robustness-wording",
      "generated-copy-marker",
    ]);
  });

  it("detects deterministic slop signals without executing code", () => {
    const findings = detectSlopSmells([
      {
        path: "src/generated.ts",
        content: [
          "export const value: any = eval(input);",
          "try { run(); } catch { return null; }",
          "const token = 'demo_token_value_12345';",
          "// TODO generated by ChatGPT",
        ].join("\n"),
      },
    ]);

    expect(findings.map((finding) => finding.title)).toEqual([
      "Placeholder logic is still present",
      "Type safety was bypassed",
      "Dynamic code execution is present",
      "Secret-looking literal was detected",
      "Failure path is hidden by a catch-all fallback",
      "Generated-copy marker needs human review",
    ]);
    expect(JSON.stringify(findings)).not.toContain("demo_token_value_12345");
    expect(findings).toContainEqual(
      expect.objectContaining({
        title: "Secret-looking literal was detected",
        evidence: "[redacted-secret-like-literal]",
      }),
    );
  });

  it("supports a narrowed smell-check set", () => {
    const checks = getSlopPassSmellChecks(["unsafe-dynamic-execution"]);
    const findings = detectSlopSmells(
      [{ path: "src/a.ts", content: "eval(userInput); // TODO" }],
      checks,
    );

    expect(findings).toHaveLength(1);
    expect(findings[0]?.title).toBe("Dynamic code execution is present");
  });
});
